package br.com.clinica.domain.enums;

public enum TipoUsuario {
    ADMIN,
    MEDICO,
    RECEPCIONISTA
}


// O enum TipoUsuario representa os diferentes tipos de usuários que podem existir no sistema da clínica médica.
// ADMIN: Acesso total ao sistema, incluindo gerenciamento de usuários e configurações.
// MEDICO: Acesso ao prontuário e consultas proprias.
// RECEPCIONISTA: Acesso para agendamento de consultas e cadastros de pacientes.

